let numero1 = 90 
let numero2 = 45 

//Comparar se um é igual ao outro
console.log(numero1 === numero2) //FALSE 

//Compara se um é diferente do outro
console.log(numero1 !== numero2) //TRUE

//Compara se um é maior que o outro
console.log(numero1 > numero2) //TRUE

//Compara se um é maior OU igual o outro
console.log(numero1 >= numero2) //TRUE

//Compara se um é menor que outro
console.log(numero1 < numero2) //FALSE

//Compara se um é menor OU igual o outro
console.log(numero1 <= numero2) //FALSE